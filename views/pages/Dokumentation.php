    <h2><a id="Design"></a>5. Design</h2>
    <h3>Layout</h3>
    <p>
        Navigation<br>
        Wir sind uns sicher, dass die Navigation am linken Rand am besten aufgehoben ist und auf dem Viewport angeheftet sein soll.
        Dies ermöglicht schnelle Sprünge zwischen den verschiedenen Seitenfunktionalitäten. Außerdem werden Endgeräte im Hochformat nicht benachteiligt,
        da wir niemals auf horizontales scrollen angewiesen sind.
        Wie auf der Moodle-Seite wird die Navigation auf die Icons reduziert, sobald die Breite des Viewports es erfordert.
    </p>
    <p>
        Header & Footer<br>
        Abgesehen vom User-Icon, über das man sich ein- und ausloggen und seine Stammdaten bearbeiten kann, ist der Header für die Funktionalität der Seite nicht relevant.
        Er schafft allerdings einen optischen Rahmen, der aus unserer Sicht maßgeblich zur Identität der Website beiträgt.
        Der Footer schließt diesen Rahmen und bietet Platz für die Verlinkung zu statischen Seiten wie etwa dem Impressum.
        Da wir den Fokus auf Funktionalität und Informationsgehalt legen, können beide aus dem Bild scrollen, um den Platz für Inhalte freizuräumen.
    </p>
    <h3>Farben</h3>
    <p>
        Das Farbschema soll möglichst simpel gehalten bleiben, um den Nutzer nicht von Inhalt & Funktionalität abzulenken.<br>
        Zu Beginn hatten wir an einigen Stellen auch mit dunkelgrauer Schrift gearbeitet, diese jedoch auf Rat auch weiß gefärbt, um die Lesbarkeit und die Kontraste zum Hintergrund zu verbessern.<br>
        Wir nutzen den selben Grauton für den Hintergrund der Seite und vieler Elemente, den auch Discord primär einsetzt.
        Diesen Farbton empfinden wir als neutral, auch auf großen Flächen angenehm zu betrachten und vor allem bietet er tolle Kontraste - vor allem zu Farben, die nicht zu grell strahlen.<br>
        Das außerdem fast überall eingesetzte hellblau ist das Ergebnis zahlreicher Experimente, die sich über die gesamte Farbpalette erstreckten. Die weiße Schrift ist darauf sehr gut lesbar und
        es gibt der Website ein modernes und vor allem technisches Gefühl, welches wir gern vermitteln möchten, da wir die Seite hauptsächlich als ein Werkzeug sehen.<br>
        Beim Vergleich der Statistiken können maximal 13 verschiedene Farben für die Diagramme benötigt werden (wenn man die Ausgabenverteilung auf Unterkategorien vergleicht und sich diese in den gewählten
        Zeiträumen grundlegend unterscheidet). Dafür wurden mit sehr viel Arbeit 13 Farben ausgewählt, die unserer Meinung nach alle miteinander harmonieren.
    </p>
    <h3>Schriften</h3>
    <p>
        Die primär verwendete Schrift ist 'Bahnschrift'. Sie bietet eine sehr gute Lesbarkeit, wirkt dabei aber deutlich moderner als Arial oder Times New Roman. Trotz der vielen Rundungen wirkt sie unglaublich
        akkurat und praktisch (was wohl auch bei ihrem Entwurf die Priorität war).
        Außerdem wird sie großflächig unterstützt und bedarf nicht der Einbindung als Webfont.<br>
        Zusätzlich haben wir 'Montserrat' als Google-Font eingebunden. Aktuell wird sie nur für die Beschriftung innerhalb der Kreisdiagramme verwendet, wo sie die Geldbeträge noch etwas futuristischer gestaltet
        und so die Aufmerksamkeit auf diese Beträge zieht. Ihre größte Stärke entfaltet sie jedoch, wenn man sie mit Kapitälchen verwendet - dies wird vermutlich in späterer Weiterentwicklung des Projekts noch Einsatz finden.<br>
        Bei allen Eingabefeldern werden Placeholder in kursiv und auto-fill-Daten in Monospace dargestellt, um die Unterscheidung von eigenen Eingaben deutlich zu machen.
    </p>
    <h3>Responsive Design</h3>
    <p>
        Da nahezu alle Elemente und Container flex-Elemente und -Boxen sind und wir uns an jeder Stelle Gedanken über das Wrap-Verhalten und die Skalierung gemacht haben, wird die Seite sich an jede gängige
        Viewport-Größe anpassen können. Bei < 498px Breite sehen einige Formulare nicht mehr symmetrisch aus, können aber bis 150px Gesamtbreite noch genutzt werden.
    </p>
</article>

