                <p>Die CoronaHealthcare ist ein Unternehmen mit langjähriger Erfahrung bei Produktion und Vertrieb von Hygieneartikeln
                    aller Art. 1992, kurz nach der Wende begann der Firmengründer, Arnold Ruppert mit der Erfindung und späteren
                    Eigenproduktion einer stark saugfähigen Damenbinde, in einer Garage in Weimar. Er gründete die Firma „SaugWeg
                    - Damenbinden“. Die damals einzigen Angestellten waren seine Frau, Lisa Fischer und der Hausmeister, Martin Seebär.
                    Schnell wurde klar, dass die Räumlichkeiten zu klein wurden und man mietete sich in eine alte Lagerhalle ein. Mit
                    stattlichen 87 Mitarbeitern fusionierte die SaugWeg GmbH nach 10 Jahren mit einem Kleinstunternehmen aus Apolda und
                    es entstand die GermanHealtcare GmbH. Fortan beschäftigte man sich neben der Produktion von Damenhygieneartikeln ebenfalls
                    mit klinischem Hygienebedarf und erweiterte das Portfolio durch fremdproduzierte Ware. Die GermanHealtcare GmbH wuchs
                    stetig und 2018 übernahm der erstgeborene Sohn von Arnold und Lisa Ruppert die Firma. Marvin Nottrott war als Inhaber
                    erfolgreich und erschloss einen zweiten Standort in Hamburg. Neben diesem entstand in Erfurt mit einem Neubau die
                    Hauptzentrale des Unternehmens. Die Geschäfte führte zukünftig sein Stellvertreter, Joseph Lindner – ein guter Freund
                    der Familie und Unternehmer. Mit Aufkommen der Pandemie nannte man sich in CoronaHealtcare um, damit die Identifikation
                    und der Fokus zu 100% auf die Eindämmung der Pandemie gelegt werden können. Jeder sollte wissen, dass CH als Unternehmen
