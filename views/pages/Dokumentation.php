    <h2><a id="Besonderheiten"></a>10. Besonderheiten</h2>
    <ul>
        <li>Alle Tabellen sind durch Klick in die Spaltenköpfe sortierbar</li>
        <li>Bei Kreisdiagrammen werden aus Übersichtlichkeits- bzw. Lesbarkeitszwecken maximal 7 Anteile dargestellt, falls die Datenfülle dies überschreiten würde, werden 6 Anteile herangezogen und der Rest als 'Sonstige' zusammengefasst</li>
        <li>Versucht ein User eine Subkategorie hinzuzufügen, die ein anderer schon erstellt hat, wird keine neue in der DB angelegt, sondern beide teilen sich diese</li>
        <li>Beim Löschen einer Subkategorie wird entsprechend auch nur die Referenz gelöscht, wodurch die Integrität bei vergangenen Buchungen erhalten bleibt</li>
    </ul>
